                    <li>
                        Any employee or volunteer who strikes a child or another adult, subjects a child to any form of 
                        corporal punishment or exhibits inappropriate behavior in sole judgment of the Director shall be 
                        subject to immediate dismissal. Employees must notify the Director or Owner if they need to take 
                        a break from their classroom due to feeling overwhelmed or frustrated.
                    </li>
                    <li>
                        Subjecting a child or an adult to humiliation or ridicule, abusive language, depriving a child of a meal, 
                        or jeopardizing the children's safety shall be considered a grave error in judgment and shall constitute 
                        grounds for immediate dismissal. Employees must notify the Director or Owner if they need to take a break 
                        from their classroom due to feeling overwhelmed or frustrated. 
                    </li>
                </ul>
            </div>
            <div class="mt-3 text-sm text-justify break-normal">
                What should you do if frustrated with a child's behavior or events in the classroom?
            </div>
            <div class="flex justify-center mt-3">
                <ul class="list-disc list-inside">
                    <li>
                        Staff will never use any form of physical punishment, including spanking on any child.
                    </li>
                    <li>
                        Staff should not raise their voices to correct a child, instead go to the child, get down on 
                        their level, and speak in a calm and quiet yet firm voice.
                    </li>
                    <li>
                        Utilize the assistant teacher in 
                        the room. Take a step back and let them handle the child instead. 
                    </li>
                    <li>
                        Staff should call Owner or Director if they need to step out for a minute.  
                    </li>
                    <li>
                        Anticipate challenging behavior times in the classroom and prepare ahead strategies to handle and eliminate them.
                    </li>
                    <li>
                        When your instinct is to raise your voice, whisper instead. This will help to settle the situation, as they are expecting the loud voice.
                    </li>
                    <li>
                        If behaviors are frequent, teachers should document the challenging behaviors and the intervention methods that were attempted in a behavior tracking log
                    </li>
                    <li>
                        Focus on what to do rather than what not to do. Try saying “slow down and walk” instead of “stop running.”
                    </li>
                    <li>
                        Keep parents in the loop. You do not need to call the parent for every behavior, but if you are seeing a pattern, make sure you are communicating to the parents. When parents and teachers work together, the child benefits.
                    </li>
                </ul>
            </div>
