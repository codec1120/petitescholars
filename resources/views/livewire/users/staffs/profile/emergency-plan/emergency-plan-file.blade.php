            <div class="text-center mt-10 underline text-base"> 
                Foreword 
            </div>
            <div class="text-justify mt-10 text-sm break-normal">
                This emergency plan describes the procedures that will be used by Petite Scholars Learning Center to provide for the care and the well-being of the children under our care and our staff.
                This plan is meant to address extraordinary circumstances that threaten lives and property. The procedures outlined in this plan constitute those temporary measures that will be taken to provide the best available protection for persons under our care. 
                The plan relies on the organization and procedures that are followed on a day-to-day basis. The intent is not to introduce new ways of doing things during high-stress situations.
            </div>
            <div class="text-justify mt-5 text-sm break-normal">
                Much of the information that is needed to implement a plan like this one should be treated as sensitive. The exact locations of shelters and assembly areas and the routes to be taken during an evacuation may be useful information to potential criminals. For this reason, 
                parts of the plan will not be released to the general public. Important details from the plan are sent home with parents in orientation materials and periodic mailings. The entire plan is available for parents to review in the facility.
            </div>
            <div class="text-justify mt-5 text-sm break-normal">
                The plan itself is organized into three parts; PART I, the "Basic Emergency Plan"; PART II, a series of checklists and a PART Ill , series of supporting documents. The Basic Emergency Plan provides overall concepts and assignment of responsibility . 'It does not contain great amounts of detail. 
                The detail in the attachments and checklists should be confidential. The information in the checklists is arranged by function, recognizing that the evacuation planned for a HAZMAT spill will work just as well for a winter storm (and probably gets practiced several times each year.)
            </div>
            <div class="text-justify mt-5 text-sm break-normal">
                Public safety officials should be aware of the provisions of this plan. The Department of Public Welfare licensing representative will also review the plan when inspecting the facility. The responsibility of the day care facility is to maintain and implement the plan. A current copy of the plan will be provided to the county or local emergency management agency
            </div>
